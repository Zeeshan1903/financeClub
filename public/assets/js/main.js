// Schedule
function updateScheduleDisplay(scheduleShow, scheduleHide) {
    // Hide elements
    scheduleHide.forEach(className => {
        const elements = document.querySelectorAll(`.${className}`);
        elements.forEach(element => {
            element.style.display = 'none';
        });
    });

    // Show elements and update content
    scheduleShow.forEach(item => {
        const elements = document.querySelectorAll(`.${item.className}`);
        elements.forEach(element => {
            element.style.display = 'block';

            // Update info
            if (item.infoText) {
                const infoElement = element.querySelector('.schedule-info span');
                if (infoElement) {
                    infoElement.textContent = item.infoText;
                }
            }

            // Update title
            if (item.profName) {
                const titleElement = element.querySelector('.schedule-title');
                if (titleElement) {
                    titleElement.innerHTML = `<span>Talk By</span><br>${item.profName}`;
                }
            }

            // Update Details
            const detailsElement = element.querySelector('.schedule-details');
            if (item.org) {
                if (detailsElement) {
                    detailsElement.innerHTML = `<span>${item.org}</sapn>`;
                }
            }

            // Update Topic
            if (item.topic) {
                if (detailsElement) {
                    detailsElement.innerHTML += `<span><br>Topic: ${item.topic}</span>`;
                }
            }

            if (item.venue) {
                if (detailsElement) {
                    detailsElement.innerHTML += `<br>Venue: ${item.venue}`;
                }
            }
        });
    });
}

